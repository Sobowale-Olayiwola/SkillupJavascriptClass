/*Recall rainbow colours with this mnemonic
ROYGBIV which represents
R-Red
O-Orange
Y-Yellow
G-Green
B-blue
I-Indigo
V-Violet
*/
function getFavouriteRainbowColour(colour){
  if(colour == "Red" || colour == "red"){
    console.log("I love colour blue");
  }else if(colour == "Orange" || colour == "orange"){
    console.log("I love colour orange");
  }else if(colour == "Yellow" || colour == "yellow"){
    console.log("I love colour yellow");
  }else if(colour == "Green" || colour == "green"){
    console.log("I love colour green");
  }else if(colour == "Blue" || colour == "blue"){
    console.log("I love colour blue");
  }else if(colour == "Indigo" || colour == "indigo"){
    console.log("I love colour indigo");
  }else if(colour == "Violet" || colour == "violet"){
    console.log("I love colour violet");
  }else{
    console.log("I do not like any rainbow color");
  }
}
getFavouriteRainbowColour("red");
getFavouriteRainbowColour("yellow");
getFavouriteRainbowColour("indigo");
